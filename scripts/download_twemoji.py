def emoji_to_filename(emoji: str) -> str:
    """Конвертирует эмодзи в имя файла Twemoji (без .png)."""
    codepoints = [f"{ord(c):x}" for c in emoji if ord(c) != 0xFE0F]
    return "-".join(codepoints)


def download_all() -> None:
    OUT_DIR.mkdir(exist_ok=True)
    total = len(EMOJIS)
    ok = 0
    fail = 0

    for i, emoji in enumerate(EMOJIS, 1):
        filename = emoji_to_filename(emoji)
        out_path = OUT_DIR / f"{filename}.png"

        if out_path.exists():
            ok += 1
            print(f"[{i}/{total}] уже есть: {filename}.png")
            continue

        url = f"{TWEMOJI_BASE}{filename}.png"
        try:
            resp = requests.get(url, timeout=10)
            if resp.status_code == 200:
                out_path.write_bytes(resp.content)
                ok += 1
                print(f"[{i}/{total}] ✓ {filename}.png")
            else:
                fail += 1
                print(f"[{i}/{total}] ✗ {filename}.png  (HTTP {resp.status_code})")
        except Exception as e:
            fail += 1
            print(f"[{i}/{total}] ✗ {filename}.png  ({e})")

        time.sleep(0.05)  # небольшая пауза чтобы не нагружать CDN

    print(f"\nГотово: {ok} скачано, {fail} ошибок → папка {OUT_DIR}")


if __name__ == "__main__":
    download_all()
